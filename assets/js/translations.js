// Get current language from localStorage or default to English
function getCurrentLanguage() {
    return localStorage.getItem('selectedLanguage') || 'en';
}

// Change language
function changeLanguage(lang) {
    if (lang in translations) {
        localStorage.setItem('selectedLanguage', lang);
        applyTranslations(lang);
        updateLanguageButton(lang);
    }
    
    // Close the dropdown after selection
    closeLanguageMenu();
    
    return false;
}

// Apply translations to the page
function applyTranslations(lang) {
    // Translate all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // Update language button
    updateLanguageButton(lang);
}

// Update language button text and highlight current language
function updateLanguageButton(lang) {
    const languageNames = {
        en: 'English',
        'en-us': 'English (US)',
        es: 'Español',
        tl: 'Tagalog',
        fr: 'Français',
        de: 'Deutsch',
        pt: 'Português',
        it: 'Italiano',
        nl: 'Nederlands',
        ru: 'Русский',
        ja: '日本語',
        zh: '中文',
        ko: '한국어',
        vi: 'Tiếng Việt',
        id: 'Bahasa Indonesia'
    };
    
    // Update button text
    const currentLanguageSpan = document.getElementById('currentLanguage');
    if (currentLanguageSpan) {
        currentLanguageSpan.textContent = languageNames[lang] || 'English';
    }
    
    // Update dropdown items - highlight selected language
    const langOptions = document.querySelectorAll('.lang-option');
    langOptions.forEach(option => {
        const optionLang = option.getAttribute('data-lang');
        if (optionLang === lang) {
            // Current language - highlight with success color
            option.classList.remove('btn-outline-secondary', 'btn-outline-success');
            option.classList.add('btn-success');
            option.style.color = '#fff';
        } else {
            // Other languages - normal styling
            option.classList.remove('btn-success');
            if (option.classList.contains('row')) {
                option.classList.add('btn-outline-success');
            } else {
                option.classList.add('btn-outline-secondary');
            }
            option.style.color = '';
        }
    });
}

// Language Selection Modal
function initLanguageModal() {
    const languageToggle = document.getElementById('languageToggle');
    const languageModal = document.getElementById('languageModal');
    const langOptions = document.querySelectorAll('.lang-option');
    
    if (!languageToggle || !languageModal) return;
    
    // Open modal when clicking language button
    languageToggle.addEventListener('click', function(e) {
        e.preventDefault();
        e.stopPropagation();
        const modal = new bootstrap.Modal(languageModal);
        modal.show();
    });
    
    // Handle language selection
    langOptions.forEach(option => {
        option.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            const lang = this.getAttribute('data-lang');
            if (lang) {
                changeLanguage(lang);
                // Close modal after selection
                const modal = bootstrap.Modal.getInstance(languageModal);
                if (modal) {
                    modal.hide();
                }
            }
        });
    });
    
    // Close on escape key
    document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape') {
            const modal = bootstrap.Modal.getInstance(languageModal);
            if (modal) {
                modal.hide();
            }
        }
    });
}

// Close language menu (kept for compatibility)
function closeLanguageMenu() {
    const languageModal = document.getElementById('languageModal');
    if (languageModal) {
        const modal = bootstrap.Modal.getInstance(languageModal);
        if (modal) {
            modal.hide();
        }
    }
}

// Safe language change handler with event stopping
function safeChangeLanguage(lang, event) {
    if (event) {
        event.preventDefault();
        event.stopPropagation();
        event.stopImmediatePropagation();
    }
    changeLanguage(lang);
    return false;
}

// Lock and unlock viewport (minimal - don't add padding which causes shifts)
function lockViewportWidth() {
    // Just prevent body scroll, don't add padding which shifts layout
    document.body.style.overflow = 'hidden';
}

function unlockViewportWidth() {
    document.body.style.overflow = '';
}

// Initialize translations on page load
document.addEventListener('DOMContentLoaded', function() {
    const currentLang = getCurrentLanguage();
    applyTranslations(currentLang);
    
    // Initialize language modal
    try {
        initLanguageModal();
    } catch (e) {
        console.log('Language modal initialization note:', e);
    }
});
